import math                                                                # importa a biblioteca math

usuarios = int(input("Informe a quantidade de usuários: "))                # recebe o número de usuários

usuarios_raiz = math.isqrt(usuarios)                                       # calcula a raíz quadrada dos usuários

print("A raíz quadrada dos seu número de funcionários é: ", usuarios_raiz) # imprime a raíz quadrada dos usuários